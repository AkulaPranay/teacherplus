    <!-- OUR STORY -->
    <div class="section-label">OUR STORY</div>
<p>
Teacher Plus was started as a bi-monthly in 1989 by Orient Longman, mainly to reach teachers 
in schools with pedagogical tools and content that would help them keep in touch with new thinking in education. 
The magazine adopted a tabloid format, initially in two-colour and later (in 2002) graduating to part four-colour 
printed on glazed newsprint. In January 2007, the magazine was bought over by Spark-India and in June 2007, 
it became a monthly with a new design. The magazine now is a folio-sized, full colour publication with a glossy 
cover and each issue comes with a separate pull-out poster or a set of activity sheets.
</p>

<p>
From 2006 to 2010, Wipro Applying Thought in Schools (WATIS) funded Teacher Plus, underwriting development, 
production, and distribution costs. Local philanthropies including Saptaparni and Progressive Constructions 
also lent their support.
</p>

<p>
In November 2010, Teacher Plus separated from Spark-India and became a proprietary concern owned by 
Dr. Usha Raman. It is now supported by Azim Premji University, Bengaluru.
</p>

<p>
Despite the many changes, the magazine has maintained its approach and editorial standards over the past 
33 years. We have tried to remain connected to the classroom and to issues faced by teachers as educators, 
as professionals, and as members of a system that often has problems.
</p>


